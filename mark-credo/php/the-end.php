    <p class="glossary"><em><strong>Synagogue / Temple</strong></em>: The Temple in Jerusalem was the centre of ancient Israel’s worship of God. It was the place where God’s presence and rule was most strongly associated with his people, and where the priests modelled relationship with him. As Israelites spread through the surrounding territories they set up ‘synagogues’ to meet together to worship God.</p>
    <p class="glossary"><em><strong>Messiah</strong></em>: Synonym of ‘Christ’. The ‘Messiah’ was a key figure promised in the story of the Bible who would bring God’s plans to save humanity to fulfilment by being a victorious king over God’s people who would reign forever.</p>
    <p class="glossary"><em><strong>Son of God</strong></em>: Another royal term like ‘Messiah’ that comes from a promise to King David that one of his descendants would be called ‘Son’ by God himself. It became a short-hand to speak of the Messiah. The question is, given the interactions between God and Jesus in Mark, is there more to it than that?</p>
